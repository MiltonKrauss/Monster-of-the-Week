<template>
  <div class="party-view">
    <div id="ban-message" role="alert" v-if="showError">
      {{ errorMessage }}
    </div>
    <div v-if="!showError"></div>
    
    <div id="intro">
      <p>
        View Your Party or create one with four (4) Characters. You are able to
        make one Party per week.
      </p>
    </div>
    <character-list></character-list>
  </div>
</template>

<script>
import CharacterList from "../components/CharacterList.vue";

export default {
  name: "party-view",

  data() {
    return {
      showError: false,
      errorMessage: "Sorry, You have been banned.",
    };
  },

  components: {
    CharacterList,
  },

  methods: {
    showBanMessage() {
      if (this.$store.state.user.authorities[0].name === "ROLE_BAN") {
        this.showError = true;
      }
    },
  },

  created() {
    this.showBanMessage();
  },
};
</script>

